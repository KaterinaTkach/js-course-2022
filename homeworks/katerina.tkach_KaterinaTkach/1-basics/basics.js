// Створити декілька довільних переміних в файлі і далі виконати з ними наступні арефметичні операції:

// Додавання +
let num1 = 10;
let num2 = 20;
console.log(num1 + num2);
console.log('^^^this is Додавання + example^^^');

// Віднімання -
console.log(num1 - num2);
console.log('^^^this is Віднімання - example^^^');

// Множення *
console.log(num1 * num2);
console.log('^^^this is Множення * example^^^');

// Ділення /
console.log(num1 / num2);
console.log('^^^this is Ділення / example^^^');

// Остача від ділення %
let num3 = 7;
let num4 = 2;
console.log(num3 % num4);
console.log('^^^^this is Остача від ділення % example^^^');

// Піднесення до степеня **
console.log(num3 ** num4);
console.log('^^^^this is Піднесення до степеня ** example^^^');

// Операція конкатенація =)
let firstString = 'Biba';
let secondString = 'Boba';
console.log(firstString + " & " + secondString  + " together forever " );
console.log("^^^^this is Операція конкатенація =) example^^^ ");

